package academy.java.basic.section5;

public class Arithmetic {
    public static void main(String[] args) {
        var a = 5;
        var b = 3;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);
    }
}
